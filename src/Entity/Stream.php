class Stream
{
    /**
     * @var int
     *
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @var string
     *
     *@ORM\Column(type="string")
     */
    private $nomSite;






    public function __construct()
    {

    }
    public function getId(): ?int
    {
        return $this->id;
    }
    public function getNomSite(): ?string
    {
        return $this->nomSite;
    }

    public function setNomSite(string $nomSite): void
    {
        $this->nomSite = $nomSite;
    }


}
